a=int(input("enter first number:"))
b=int(input("enter second number:"))
choice=input("enter +,-,*,/:")
if choice=="+":
    print("result:",a+b)
elif choice=="-":
    print("result:",a-b)
elif choice=="*":
    print("result:",a*b)
elif choice=="/":
    print("result:",a/b)
else:
    print("invaild choice")




